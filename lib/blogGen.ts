export interface BlogRequest {
  keyword: string // 노릴 검색어 (예: 쌍문역 스터디카페)
  topic: string // 글 주제/방향
  title?: string // 사용할 제목 (제목 짓기에서 선택)
  type: '정보형' | '스토리형' | '후기형'
  photos: string[] // 넣을 사진 설명들
  extra?: string // 추가로 강조할 내용
}

export interface TitleIdea {
  title: string
  topic: string
}

